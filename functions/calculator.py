# Calculator exercise

def addition(int1, int2):
    return int1 + int2

print(addition(7, 11))

def subtraction(int1, int2):
    return int1 - int2

print(subtraction(11, 7))

def multiplication(int1, int2):
    return int1 * int2

print(multiplication(11, 7))

def division(int1, int2):
    return int1 / int2

print(division(4, 2))