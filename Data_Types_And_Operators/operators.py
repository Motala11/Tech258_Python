# Operators

x = 5
y = 1

print(x + y)

# Arithmetic operators
result = x + y + 2
print("Let's show x + y + 2 =", result)

# Subtract
result = x - y - 2
print("Let's show x - y - 2 =", result)

# Multiply
result = x * y * 2
print("Let's show x * y * 2 =", result)

# Divide
result = x / y / 2
print("Let's show x / y / 2 =", result)

# Modulus
result = x % y % 2
print("Let's show x % y % 2 =", result)

# Comparison operators - >, <, ==, !=, >=, <=

#More than
result = x > y
print(result)

#Less than
result = x < y
print(result)

# Is equal to
result = x == y
print(result)

# Is not equal to
result = x != y
print(result)

# More than or equal to
result = x >= y
print(result)

# Less than or equal to
result = x <= y
print(result)